# + - * /  加减乘除
a = 10
b = 20
c = a + b
print(c)

d = a * b
print(d)

e = a - b
print(e)

f = b / a
print(f)

# 取整除 //
a1 = 13
b1 = 3
c1 = a1 // b1
print(c1)

# 取模 取余数 % -- 判断奇偶性  如果对2取余数为0，该数则为偶数
left = 9
right = 2
result = left % right
print(result)

#  取幂  **
first = 5
second = 3
then = first ** second
print(then)

# 赋值运算符 =
a = 15
a += 15  # 等于 a = a + 10
print(a)


